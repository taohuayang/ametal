/**
 * \brief 传感器 TMF8801 初始化
 */
am_sensor_handle_t am_sensor_tmf8801_init (
        am_sensor_tmf8801_dev_t           *p_dev,
        const am_sensor_tmf8801_devinfo_t *p_devinfo,
        am_i2c_handle_t                   handle)
{
    am_err_t ret = AM_OK;
    am_err_t cur_ret = AM_OK;

    uint8_t tmf8801_id = 0;
    uint8_t reg = 0;

    if (p_dev == NULL || p_devinfo == NULL) {
        return NULL;
    }

    am_i2c_mkdev(&p_dev->i2c_dev,
                 handle,
                 p_devinfo->i2c_addr,
                 AM_I2C_ADDR_7BIT | AM_I2C_SUBADDR_1BYTE);

    p_dev->tmf8801_dev.p_drv   = p_dev;
    p_dev->tmf8801_dev.p_funcs = &__g_sensor_tmf8801_funcs;
    p_dev->dev_info            = p_devinfo;
    p_dev->pfn_trigger_cb[0]   = NULL;
    p_dev->p_arg[0]            = NULL;
    p_dev->flags[0]            = 0;
    p_dev->trigger             = 0;
    p_dev->data[0].val         = 0;
    p_dev->data[0].unit        = AM_SENSOR_UNIT_INVALID;

    if (p_devinfo->trigger_pin != -1) {
        
        am_gpio_pin_cfg(p_devinfo->trigger_pin, AM_GPIO_INPUT | AM_GPIO_PULLUP);
        am_gpio_trigger_connect(p_devinfo->trigger_pin,
                                __tmf8801_alarm_callback,
                                (void*)p_dev);
        am_gpio_trigger_cfg(p_devinfo->trigger_pin, AM_GPIO_TRIGGER_FALL);
    }

    am_isr_defer_job_init(&p_dev->g_myjob, __am_pfnvoid_t, p_dev, 1);

    /* TOF硬件复位 */
    am_gpio_pin_cfg(p_devinfo->enable_pin, AM_GPIO_OUTPUT_INIT_LOW);
    am_mdelay(50);

    /* TOF使能 */
    am_gpio_set(p_devinfo->enable_pin, AM_GPIO_LEVEL_HIGH);
    am_mdelay(50);

    /* TOF软件复位 */
    reg = __TMF8801_SW_RST;
    __tmf8801_write(p_dev, __TMF8801_REG_ENABLE, &reg, 1);
    am_mdelay(10);
    
    /* 等待复位完成 */
    do {
        __tmf8801_read(p_dev, __TMF8801_REG_ENABLE, &reg, 1);

    } while (reg & __TMF8801_SW_RST);
    
    /* 读取ID */
    cur_ret = __tmf8801_read(p_dev, __TMF8801_REG_ID, &tmf8801_id, 1);

    if (cur_ret != AM_OK || (tmf8801_id & __TMF8801_ID) != __TMF8801_ID) {
        am_kprintf("\r\nSensor TMF8801 Init is ERROR! \r\n");
        return NULL;
    }

    /* 初始化，写入APP并复位 */
    ret = __tmf8801_init(p_dev);
    if (ret != AM_OK) {
        cur_ret = ret;
    }

    /* 执行工厂校准 */
    ret = __tmf8801_factory_calibration(p_dev);
    if (ret != AM_OK) {
        cur_ret = ret;
    }

    if (cur_ret != AM_OK) {
        return NULL;
    }

    return &(p_dev->tmf8801_dev);
}

/**
 * \brief 传感器 TMF8801 去初始化
 */
am_err_t am_sensor_tmf8801_deinit (am_sensor_handle_t handle)
{
    am_sensor_tmf8801_dev_t *p_dev = handle->p_drv;

    if (handle == NULL) {
        return -AM_EINVAL;
    }

    p_dev->tmf8801_dev.p_drv   = NULL;
    p_dev->tmf8801_dev.p_funcs = NULL;
    p_dev->dev_info           = NULL;

    return AM_OK;
}

