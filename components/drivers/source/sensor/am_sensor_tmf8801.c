/**
 * \brief 读取 TMF8801 状态标志位 
 *
 * \retval AM_OK    : 操作成功
 * \retval AM_ERROR : 操作失败
 */
static int8_t __tmf8801_status_read (am_sensor_tmf8801_dev_t *p_this)
{
    uint8_t sta_buf[3] = {0};
    
    /* 读状态寄存器 */
    __tmf8801_read(p_this, __TMF8801_REG_BL_CMD_STAT, sta_buf, 3);

    if (sta_buf[0] != 0x00 ||
        sta_buf[1] != 0x00 ||
        sta_buf[2] != 0xFF) 
    {
        return AM_ERROR;
    }
    return AM_OK;
}

/**
 * \brief 初始化下载 HW 
 */
static int8_t __tmf8801_download_init (am_sensor_tmf8801_dev_t *p_this)
{
    /*
     * cmd_buf[0]    : 初始化下载 HW 命令(0x14)
     * cmd_buf[1]    : 数据长度(0x01)
     * cmd_buf[3]    : 1个字节数据
     * cmd_buf[4]    : 校验字节
     */
    uint8_t  cmd_buf[] = {0x14, 0x01, 0x29, 0xC1};
    uint32_t tick = 0;
    
    __tmf8801_write(p_this, __TMF8801_REG_BL_CMD_STAT, cmd_buf, 4);
    
    while (1) 
    {
        if (__tmf8801_status_read(p_this) == AM_OK) 
        {
            break;
        }
        tick ++;
        if (tick >= 10) 
        {
#if __TMF8801_DEBUG
            printf("__tmf8801_download_init error!\r\n");
#endif
            return AM_ERROR;
        }
    }
    return AM_OK;
}

/**
 * \brief 设置写 RAM 地址
 */
static int8_t __tmf8801_set_ram_addr (am_sensor_tmf8801_dev_t *p_this,
                                      uint16_t                 addr)
{
    /*
     * cmd_buf[0]    : 写 RAM 地址命令(0x43)
     * cmd_buf[1]    : 数据长度(0x02)
     * cmd_buf[4:3]  : 2个字节数据
     * cmd_buf[5]    : 校验字节
     */
    uint8_t  cmd_buf[5] = {0};
    uint32_t tick       =  0;
    
    cmd_buf[0] = 0x43;                  
    cmd_buf[1] = 0x02;
    cmd_buf[2] = (uint8_t)addr;         /* 地址低 8 位 */
    cmd_buf[3] = (uint8_t)(addr >> 8);  /* 地址高 8 位 */
    cmd_buf[4] = (cmd_buf[0]            /* 校验字节 */ 
               +  cmd_buf[1]
               +  cmd_buf[2]
               +  cmd_buf[3])
               ^ 0xFF;
             
    __tmf8801_write(p_this, __TMF8801_REG_BL_CMD_STAT, cmd_buf, 5);
    
    while (1) 
    {
        if (__tmf8801_status_read(p_this) == AM_OK) 
        {
            break;
        }
        tick ++;
        if (tick >= 10) 
        {
#if __TMF8801_DEBUG
            printf("__tmf8801_set_ram_addr error!\r\n");
#endif
            return AM_ERROR;
        }
    }
    return AM_OK;
}

/**
 * \brief 写 RAM 
 */
static int8_t __tmf8801_write_ram (am_sensor_tmf8801_dev_t *p_this)
{   
    /*
     * cmd_buf[0]    : 命令(0x41)
     * cmd_buf[1]    : 数据长度(0x10)
     * cmd_buf[17:2] : 16个字节数据
     * cmd_buf[18]   : 校验字节
     */
    uint8_t  cmd_buf[19] = {0};  
    
    uint16_t buf_length  =  0;                  /* __g_app_buf 总字节个数 */ 
    uint16_t buf_line    =  0;                  /* __g_app_buf 行地址 */
    
    uint32_t tick        =  0;
    uint16_t i           =  0;
    uint16_t j           =  0;
    
    buf_length = sizeof(__g_app_buf) / sizeof(__g_app_buf[0]);
    buf_line   = buf_length / 16;
    
    cmd_buf[0] = 0x41;                          /* 写 RAM */
    cmd_buf[1] = 0x10;                          /* 待写数据长度:16字节 */
    
    for (i = 0; i < buf_line; i++) {
    
        /* 装载命令数据缓存区 */
        for (j = 0; j < 16; j++) {              /* 16字节 */
        
            cmd_buf[2+j] = __g_app_buf[16 * i + j]; 
        }
        
        /* 计算校验字节 */
        cmd_buf[18] = 0;                        /* 清零校验字节 */
        for (j = 0; j < 18; j++) {              /* 累加 */
        
            cmd_buf[18] += cmd_buf[j];
        }
        cmd_buf[18] ^= 0xFF;                    /* 异或 0xFF */

        /* 设置RAM地址 */
        __tmf8801_set_ram_addr(p_this, (16 * i));

        /* 写 RAM */
        __tmf8801_write(p_this, __TMF8801_REG_BL_CMD_STAT, cmd_buf, 19);

        /* 等待状态就绪 */
        am_udelay(150);

        /* 读状态寄存器 */
        while (1) 
        {
            if (__tmf8801_status_read(p_this) == AM_OK) 
            {
                break;
            }
            tick ++;
            if (tick >= 10)
            {
#if __TMF8801_DEBUG
                printf("__tmf8801_write_ram error!\r\n");
#endif
                return AM_ERROR;
            }
        } 
    } 
    return AM_OK;
}

/**
 * \brief 重映射 RAM 到地址 0 并复位
 */
static int8_t __tmf8801_ram_remap_rst (am_sensor_tmf8801_dev_t *p_this)
{
    /*
     * cmd_buf[0]    : RAM 地址重映射并复位命令(0x11)
     * cmd_buf[1]    : 数据长度(0x00)
     * cmd_buf[2]    : 校验字节
     */
    uint8_t  cmd_buf[] = {0x11, 0x00, 0xEE};       
    uint8_t  regval    =  0;
    uint32_t tick      =  0;
    
    __tmf8801_write(p_this, __TMF8801_REG_BL_CMD_STAT, cmd_buf, 3);

    /* 等待状态就绪 */
    am_udelay(150);
    
    while (1) 
    {
        /* wait CPU ready */
        __tmf8801_read(p_this, __TMF8801_REG_ENABLE, &regval, 1);
        if (regval == 0x41) 
        {
            break;
        }
        tick ++;
        if (tick >= 10) 
        {
#if __TMF8801_DEBUG
            printf("__tmf8801_ram_remap_rst error!\r\n");
#endif
            return AM_ERROR;
        }
    }
    return AM_OK; 
}

